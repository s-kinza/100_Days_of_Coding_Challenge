// THIS FUNCTION FIND THE LEFTOVER OF DIVIDING TWO NUMBERS

function remainder(num1: number, num2: number): number{

    return num1 % num2
}

console.log(remainder(0, 2));
console.log(remainder(5, 2));
console.log(remainder(8, 6));
console.log(remainder(10, 7));