function useCompoundOperators () {
    let x = 4;
    console.log('\nINITIAL VALUE x:', x);

    x += 2;
    console.log('\nAFTER ADDITION x:', x);

    x -= 1;
    console.log('\nAFTER SUBTRACTION x:', x);

    x *= 2;
    console.log('\nAFTER MULTIPLICATION x:', x);

    x /= 5;
    console.log('\nAFTER DIVIDING x:', x);
}
useCompoundOperators();